package CollectionFrameWorkSorting.ByHiteshSir;

public class Student_Mx {
	private int rollno;
	private String name;
	private int marks;
	public Student_Mx(int rollno, String name, int marks) {
		super();
		this.rollno = rollno;
		this.name = name;
		this.marks = marks;
	}
	@Override
	public String toString() {
		return "Student_Mx [rollno= " + rollno + ", name= " + name + ", marks= " + marks ;
	}
	public int getRollno() {
		return rollno;
	}
	public String getName() {
		return name;
	}
	public int getMarks() {
		return marks;
	}
	
	
	
	
	
	
	

}
